    private void btnCreateReportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateReportActionPerformed
        
        try{
            if(validateForm()){                                                           
               UsageReportViewModel viewModel= getServiceClient().getUsageReport(safeStartDate, safeEndDate, getSafeDelimiter());
               tblReport.setModel(new UsageReportTableModel((viewModel)));
            }else{
               eventAggregator.publish(new StatusEvent("Dates are invalid.")); 
            }    
            
        }catch(Exception ex){
            handleException(ex);
        }
        
    }//GEN-LAST:event_btnCreateReportActionPerformed

    private int getSafeDelimiter(){
        switch(delimiter){
                   case DAY : return Calendar.DATE; 
                   case MONTH : return Calendar.MONTH;
                   case YEAR : return Calendar.YEAR;
               }
        return -1;
    }
    
