    private boolean validateForm() {
        try{
            safeStartDate = dateFormatter.parse(getStartDate());
            safeEndDate = dateFormatter.parse(getEndDate());
            
            if(safeEndDate.before(safeStartDate))
                return false;            
                
            return true;                  
            
        }catch(Exception ex){
            return false;
        }
    }
    
   
    
